//==============================================================
//			初期化
//--------------------------------------------------------------
//	●引数
//			無し
//	●返値
//			無し
//==============================================================
void	SHA224::init(void)
{
	iCountBlock = 0;
	H[0] = 0xC1059ED8;
	H[1] = 0x367CD507;
	H[2] = 0x3070DD17;
	H[3] = 0xF70E5939;
	H[4] = 0xFFC00B31;
	H[5] = 0x68581511;
	H[6] = 0x64F98FA7;
	H[7] = 0xBEFA4FA4;
}
//==============================================================
//			ハッシュ値取得
//--------------------------------------------------------------
//	●引数
//			void *result	ハッシュ値を格納するアドレス（224bit）
//	●返値
//			無し
//==============================================================
void	SHA224::getHash(void *result)
{
	memcpy(result,H,SHA224_HashSize);
}
