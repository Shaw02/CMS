#include "StdAfx.h"
#include "HMAC-SHA-256.h"

unsigned	int		HMAC_SHA256::oid[] = {1,2,840,113549,2,9};	//RFC 4231

//==============================================================
//			コンストラクタ
//--------------------------------------------------------------
//	●引数
//			無し
//	●返値
//			無し
//==============================================================
HMAC_SHA256::HMAC_SHA256(SHA256* _cSHA256, const char _strName[]):
	HMAC(_cSHA256, _strName)
{
	Set_oid(oid,sizeof(oid)/sizeof(int));
	Set_HMAC_SHA256();
}
//==============================================================
//			デストラクタ
//--------------------------------------------------------------
//	●引数
//			無し
//	●返値
//			無し
//==============================================================
HMAC_SHA256::~HMAC_SHA256(void)
{
}
//==============================================================
//		ASN.1構造体の設定
//--------------------------------------------------------------
//	●引数
//			無し
//	●返値
//			無し
//==============================================================
void	HMAC_SHA256::Set_HMAC_SHA256(void)
{
	//ASN.1の定義
	Set();
	Set_Construct(&null);
}