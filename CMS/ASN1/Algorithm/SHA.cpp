#include "StdAfx.h"
#include "SHA.h"

//==============================================================
//			コンストラクタ
//--------------------------------------------------------------
//	●引数
//			無し
//	●返値
//			無し
//==============================================================
SHA::SHA(const char _strName[]):
	Digest(_strName)
{
	szBlock = SHA_BlockSize;
	M = new unsigned char [szBlock];	//確保はここでする。
}
//==============================================================
//			デストラクタ
//--------------------------------------------------------------
//	●引数
//			無し
//	●返値
//			無し
//==============================================================
SHA::~SHA(void)
{
	delete	M;
}
